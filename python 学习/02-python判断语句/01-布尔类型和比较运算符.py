# 定义变量存储布尔类型的数据
bool_1 = True
bool_2 = False
print(f"bool_1的内容是{bool_1}，类型是{type(bool_1)}")
print(f"bool_2的内容是{bool_2}，类型是{type(bool_2)}")

# 比较运算符的使用
#  ==,!=,>,<,>=,<=
print(f"10 == 12的结果是{10 == 12}")
print(f"10 != 12的结果是{10 != 12}")

name1 = "张三"
name2 = "李四"
print(f"name1 == name2的结果是{name1 == name2}")

# 大于小于，大于等于小于等于
print(f"10 > 12的结果是{10 > 12}")
print(f"10 < 12的结果是{10 < 12}")
print(f"10 >= 10的结果是{10 >= 10}")
print(f"10 <= 10的结果是{10 <= 10}")
